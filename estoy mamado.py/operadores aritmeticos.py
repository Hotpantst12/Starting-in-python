

# examples of an arithmetic operator

a = 9

b = 4

# addition of numbers

add = a + b

# subtraction of numbers

sub = a - b

# multiplicacion

mul = a * b

# division (float) of number

div1 = a / b

# division(floor) of number

div2 = a // b

# modulo of both number

mod = a % b

# power

p = a ** b

# print results

print(add)

print(sub)

print(mul)

print(div1)

print(div2)

print(mod)

print(p)

